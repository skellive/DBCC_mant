    private void jBENTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBENTRARActionPerformed

        UsuariosDAO userd = new UsuariosDAO();
        UsuariosVO userv = new UsuariosVO();

        String pass = new String(txtPassword.getPassword());

        if (!txtUser.getText().equals("") && !pass.equals("")) {
            String nuevoPass = hash.sha1(pass);
            userv.setUsuario(txtUser.getText());
            userv.setContrasenia(nuevoPass);

            if (userd.login(userv)) {
                Principal2 prin = new Principal2(userv);

                prin.setVisible(true);
                this.setVisible(false);

            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o contrase√±a incorrectos");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Campos vacios, ingrese sus datos correctamente");
        }


    }//GEN-LAST:event_jBENTRARActionPerformed

